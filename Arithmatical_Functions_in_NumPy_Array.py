import numpy as np

# Arithematical Functions for 1D Array:- 
variable= np.array([11,12,13,14])
print("The output of min() function is:",np.min(variable)) # Use of min() Function.
print("\n")
print("The output of max() function is:",np.max(variable))# Use of min() Function.
print("\n")
print("The output of sqrt() function is:",np.sqrt(variable))# Use of sqrt() Function.
print("\n")
print("The output of argmin() function is:",np.argmin(variable))# Use of argmin() Function.
print("\n")
print("The output of argmax() function is:",np.argmax(variable))# Use of argmax() Function.
print("\n")
print("The output of sin() function is:",np.sin(variable))# Use of sin() Function.
print("\n")
print("The output of cos() function is:",np.cos(variable))# Use of cos() Function.
print("\n")
print("The output of cumsum() function is:",np.cumsum(variable))# Use of cumsum() Function.
print("\n")




