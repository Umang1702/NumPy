# Performing Arithematic Operations On NumPy Arrays on Higher Dimensional  Arrays:-
import numpy as np # Importing Numpy library
variable1=np.array([1,2,3,4],ndmin=10)  #Intialise Higher Dimensional Numpy array.
variable2=np.array([5,6,7,8],ndmin=10)
print("The Sum of two array is:",variable1+variable2) # Addition
print("\n")
print("The Difference of two array is:",variable1-variable2) # Subtraction
print("\n")
print("The Divide of two array is:",variable1/variable2) # Divide
print("\n")
print("The Multiplication of two array is:",variable1*variable2) # Multiplication
print("\n")
print("The Modulus of two array is:",variable1%variable2) # Modulus
